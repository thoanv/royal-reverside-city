<p dir="ltr">Ở River Cottage không gian rất yên bình, view bên ngoài cửa sổ có cây cối xanh mát rất thơ mộng và mát mắt. Tuy là một khách sạn không quá cao cấp nhưng River Cottage luôn được khách hàng đánh giá rất cao vì rất tỉ mỉ, chỉn chu, nhân viên ân cần chu đáo và rất lịch sự.&nbsp;</p>
